#include <stdio.h>

int soma(int a, int b){
    return (a + b);
}

int subtracao(int a, int b){
    return (a - b);
}

int multiplicacao(int a, int b){
    return (a * b);
}

float divisao(float a, float b){
    return (a / b);
}
